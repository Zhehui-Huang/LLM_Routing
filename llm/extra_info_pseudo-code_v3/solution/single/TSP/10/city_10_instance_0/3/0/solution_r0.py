# Euclidean distance between two points
def distance(p1, p2):
    return math.sqrt((p1[0] - p2[0])**2 + (p1[1] - p2[1])**2)

# Calculate weights (distances) between all pairs of cities
weights = {(i, j): distance(cities[i], cities[j]) for i in cities for j in cities if i != j}